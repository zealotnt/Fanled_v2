static volatile uint32_t TimingDelay = 0;
static volatile uint32_t Time = 0;
static volatile uint32_t Time2;

/******************************************************************************/
/* LOCAL (STATIC) FUNCTION DECLARATION SECTION                                */
/******************************************************************************/


/******************************************************************************/
