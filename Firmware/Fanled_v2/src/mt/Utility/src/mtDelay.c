void Delay(volatile uint32_t nTime)
{
	TimingDelay = nTime;
	while (TimingDelay != 0);
}

void TimingDelay_Decrement(void) 
{
	Time++;
	if (Time2 != 0x00) 
	{
		Time2--;
	}
	if (TimingDelay != 0x00) 
	{
		TimingDelay--;
	}
}

uint32_t DELAY_Time(void) 
{
	return Time;
}

uint32_t DELAY_Time2(void) 
{
	return Time2;
}

void DELAY_SetTime(volatile uint32_t time)
{
	Time = time;
}

void DELAY_SetTime2(volatile uint32_t time)
{
	Time2 = time;
}

