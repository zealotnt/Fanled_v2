void uart_write_char(char c)
{
	while(USART_GetFlagStatus(USART1, USART_FLAG_TXE) !=SET);
	USART_SendData(USART1, c);
	while(USART_GetFlagStatus(USART1, USART_FLAG_TC) !=SET);
}

void uart_write_str(char *str)
{
	while (*str)
	{
		uart_write_char(*str);
		str ++;
	}
}

